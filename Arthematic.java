package myjava;

public class Arthematic {

	public static void main(String[] args) {
		int x=10,y=5;
		
		System.out.println("Sum is "+(x+y));
		
		System.out.println("Sum is "+(x-y));
		
		System.out.println("Multiplication is "+(x*y));
		
		System.out.println("Division is "+(x/y));
		
		System.out.println("Percentile is "+(x%y));

	}

}
